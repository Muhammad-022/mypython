# Day 20: Increment and Decrement in Loops

# Increment using for loop
print("For loop with increment:")
for i in range(1, 6):
    print(i)

# Increment using while loop
print("While loop with increment:")
num = 1
while num <= 5:
    print(num)
    num = num + 1

# Decrement using for loop
print("For loop with decrement:")
for i in range(5, 0, -1):
    print(i)


# Decrement using while loop
print("While loop with decrement:")
num = 5
while num >= 1:
    print(num)
    num = num - 1
