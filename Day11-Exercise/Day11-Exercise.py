
# 1. Data Types
x = 10
y = 3.14
z = "Python"

print(type(x))
print(type(y))
print(type(z))

# 2. Type Casting
num = input("Enter a number: ")
num = int(num)
print(num + 5)

# 3. Operators
a = 10
b = 3

print(a + b, a - b, a * b, a / b)
print(a > b and b < 5)

# 4. If-Else
num = int(input("Enter a number: "))
if num > 0:
    print("Positive")
elif num < 0:
    print("Negative")
else:
    print("Zero")
