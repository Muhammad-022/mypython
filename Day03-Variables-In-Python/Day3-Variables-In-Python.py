# Variables in Python

# Integer variable
age = 20
print(age)

# Float variable
price = 99.5
print(price)

# String variable
name = "Ahmad"
print(name)

# Boolean variable
is_student = True
print(is_student)

# Updating variable
score = 50
print(score)

score = 80
print(score)

# Multiple variables
a, b, c = 1, 2, 3
print(a, b, c)

# Same value to multiple variables
x = y = z = 10
print(x, y, z)

# Checking variable types
print(type(age))
print(type(price))
print(type(name))
print(type(is_student))
